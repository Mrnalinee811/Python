score = int(input('Enter your score: '))

if score >= 90:
    print('Grade: A - Excellent!')
elif score >= 80:
    print('Grade: B - Good job!')
elif score >= 70:
    print('Grade: C - Keep trying!')
elif score >= 60:
    print('Grade: D - Need improvement!')
else:
    print('Grade: F - Please study harder!')